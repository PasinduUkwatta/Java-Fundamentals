public class Switch {
    public static void main(String [] args){

        int i = 10;

        switch (i){
            case 10:{
                System.out.println("number is 10");
                break;
            }

            case 20:{
                System.out.println("number is 20");
                break;
            }
            default:
            {
                System.out.println("this is default method");
            }
        }
    }
}
