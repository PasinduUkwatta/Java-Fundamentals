public class ForEachLoop {
    public static void main(String[]args){

        int [] arr1  = {1,2,3,4,5};
        for (int i :arr1) {
            System.out.println(i);

        }

        String [] arr2 ={"apple","mango","pinaapple","banana"};
        for (String str:arr2)
            System.out.println(str);

    }
}
