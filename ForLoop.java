public class ForLoop {
    public static void main(String []args){

        //we are using the for loop when know the no of iterations
        for (int i =0;i<=10;i++){
            System.out.println("increasing loop  ");
            System.out.println(i);
        }

        for (int i =10;i>=0;i--){
            System.out.println("decreasing loop ");
            System.out.println(i);

        }
    }
}
